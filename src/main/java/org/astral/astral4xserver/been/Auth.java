package org.astral.astral4xserver.been;

public class Auth {
    private String token;
    private String x_auth;

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getX_auth() {
        return x_auth;
    }

    public void setX_auth(String x_auth) {
        this.x_auth = x_auth;
    }
}
